#!/bin/bash
# ============================================================
#  전체 실행 스크립트
#
#  폴더 구조 (어느 경로든 상관없음):
#    <실행위치>/
#      ├── 100_run.sh          ← 이 파일
#      ├── 00_bootstrap.sh     ← 리소스그룹A 생성
#      └── 01_tuna/            ← Terraform 코드
#
#  실행 환경:
#    Git Bash 전용 (PowerShell, CMD 사용 불가)
#
#  실행:
#    bash 100_run.sh
# ============================================================

set -e

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
TF_DIR="$SCRIPT_DIR/01_tuna"

# ────────────────────────────────────────────────────────────
#  구독 ID 설정 (메모리에만 존재, 쉘 닫으면 사라짐)
# ────────────────────────────────────────────────────────────
export SUBSCRIPTION_ID=""
export TF_VAR_subid="$SUBSCRIPTION_ID"

echo "============================================"
echo "  전체 인프라 배포 시작"
echo "============================================"
echo ""

# ────────────────────────────────────────────────────────────
#  사전 확인
# ────────────────────────────────────────────────────────────
if [[ ! -f "$SCRIPT_DIR/00_bootstrap.sh" ]]; then
  echo "❌ 00_bootstrap.sh 파일을 찾을 수 없습니다."
  exit 1
fi

if [[ ! -d "$TF_DIR" ]]; then
  echo "❌ 01_tuna 폴더를 찾을 수 없습니다."
  exit 1
fi

if ! command -v terraform &>/dev/null; then
  echo "❌ terraform이 설치되어 있지 않습니다."
  exit 1
fi

if ! command -v az &>/dev/null; then
  echo "❌ az CLI가 설치되어 있지 않습니다."
  exit 1
fi

# ────────────────────────────────────────────────────────────
#  1단계: Bootstrap
# ────────────────────────────────────────────────────────────
echo "============================================"
echo "  [1단계] Bootstrap 실행"
echo "============================================"
echo ""

bash "$SCRIPT_DIR/00_bootstrap.sh"

echo ""
echo "✅ Bootstrap 완료"

# ────────────────────────────────────────────────────────────
#  2단계: Terraform
# ────────────────────────────────────────────────────────────
echo ""
echo "============================================"
echo "  [2단계] Terraform 실행"
echo "============================================"
echo ""

cd "$TF_DIR"
echo "  경로: $(pwd)"
echo ""

echo "── terraform init ──────────────────────────"
terraform init
echo ""

echo "── terraform apply ─────────────────────────"
terraform apply --auto-approve

# ────────────────────────────────────────────────────────────
#  완료
# ────────────────────────────────────────────────────────────
echo ""
echo "============================================"
echo "  ✅ 전체 배포 완료!"
echo "============================================"
echo ""
echo "  output 확인:"
echo "  cd 01_tuna && terraform output"
